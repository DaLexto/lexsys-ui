import { describe, expect, test } from "vitest"
import { componentTokens } from "../src/components/index.js"
import {
  createStyleOutputs,
  createThemeCssFromDtcgJson,
  createTokensCssFromDtcgJson,
} from "../src/generators/create-style-outputs.js"
import {
  createDtcgTokenInputFromJson,
  createStyleTokenInput,
} from "../src/generators/input/index.js"
import { colorPrimitives } from "../src/primitives/color.js"
import { primitiveTokens } from "../src/primitives/index.js"
import { semanticTokens } from "../src/semantics/index.js"
import { neurexPreset, defaultPresetId, presets } from "../src/presets/index.js"
import { themes } from "../src/themes/index.js"

const getOklchLightness = (value: unknown): number => {
  expect(typeof value).toBe("string")

  const match = String(value).match(/^oklch\((\d+(?:\.\d+)?)\s/)

  expect(match?.[1]).toBeDefined()

  return Number(match?.[1])
}

describe("createStyleOutputs", () => {
  test("assembles a W3C/DTCG-shaped generator input contract", () => {
    const input = createStyleTokenInput()

    expect(input.preset).toBe(neurexPreset)
    expect(input.foundationTokens.color).toBeDefined()
    expect(input.foundationTokens.radius).toBeDefined()
    expect(input.semanticTokens.color).toBeDefined()
    expect(input.semanticTokens.radius).toBeDefined()
    expect(input.componentTokens.button).toBeDefined()
    expect(input.tokenTree.button).toBe(input.componentTokens.button)
    expect(input.themeTokens[0]).toMatchObject({
      name: "light",
      selector: ":root",
      colorScheme: "light",
      brand: "neurex",
    })
    expect(input.themeTokens[0]?.tokens.color).toBeDefined()
  })

  test("uses one active preset when assembling generator input", () => {
    const input = createStyleTokenInput({
      presetId: "neurex",
    })

    expect(input.preset.id).toBe("neurex")
    expect(input.themeTokens.map((theme) => theme.name)).toEqual([
      "light",
      "dark",
    ])
  })

  test("defines Neurex Default as the first style preset", () => {
    expect(defaultPresetId).toBe("neurex")

    expect(neurexPreset).toEqual({
      id: "neurex",
      name: "Neurex Default",
      brand: "neurex",
      description:
        "Baseline Neurex style preset for the initial token, theme, and component system.",
      themeModes: ["light", "dark"],
      defaultTheme: "light",
    })
    expect(presets).toEqual([neurexPreset])
  })

  test("keeps the yellow primitive scale ordered from light to dark", () => {
    const yellow = colorPrimitives.yellow as Record<
      string,
      { $value?: unknown }
    >
    const shadeKeys = [
      "50",
      "100",
      "200",
      "300",
      "400",
      "500",
      "600",
      "700",
      "800",
      "900",
      "950",
    ]
    const lightnessValues = shadeKeys.map((shade) => {
      return getOklchLightness(yellow[shade]?.$value)
    })

    lightnessValues.slice(1).forEach((lightness, index) => {
      expect(lightness).toBeLessThan(lightnessValues[index] ?? Number.NaN)
    })
  })

  test("generates token and theme css from token source", () => {
    const outputs = createStyleOutputs()

    expect(outputs.tokensCss).toContain(
      "Generated by @neurex/tokens. Do not edit directly.",
    )
    expect(outputs.tokensCss).toContain("--nx-color-blue-600")
    expect(outputs.tokensCss).toContain(
      "--nx-radius-control: var(--nx-radius-md)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-space-surface-md: var(--nx-space-6)",
    )
    expect(outputs.tokensCss).toContain("--nx-line-height-normal: 1.5")
    expect(outputs.tokensCss).not.toContain("--nx-line-height-6")
    expect(outputs.tokensCss).toContain(
      "--nx-button-radius: var(--nx-radius-control)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-badge-radius: var(--nx-radius-control)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-badge-primary-background: var(--nx-color-action-primary-base)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-alert-radius: var(--nx-radius-surface)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-alert-destructive-foreground: var(--nx-color-action-danger-base)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-card-background: var(--nx-color-background-surface)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-card-radius: var(--nx-radius-surface)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-checkbox-size-md: var(--nx-size-selection-control-md)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-switch-thumb-translate-md: var(--nx-size-switch-thumb-translate-md)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-tabs-tab-active-background: var(--nx-color-background-base)",
    )
    expect(outputs.tokensCss).toContain(
      "--nx-tooltip-background: var(--nx-color-text-primary)",
    )

    expect(outputs.themeCss).toContain("--nx-color-background-base")
    expect(outputs.themeCss).toContain(".dark")
    expect(outputs.themeCss).toContain("@theme inline")
    expect(outputs.themeCss).toContain("--color-nx-action-primary-base")
    expect(outputs.themeCss).toContain("--color-nx-background-base")
    expect(outputs.themeCss).toContain("--radius-nx-control")
    expect(outputs.themeCss).toContain("--spacing-nx-surface-md")
    expect(outputs.themeCss).toContain("--duration-nx-control")
    expect(outputs.themeCss).toContain("--ease-nx-control")
    expect(outputs.themeCss).not.toContain("--color-nx-color-")
  })

  test("generates DTCG-compatible token json from token source", () => {
    const outputs = createStyleOutputs()
    const json = JSON.parse(outputs.tokensJson) as {
      $schema?: unknown
      $extensions?: {
        "org.neurex"?: {
          generatedBy?: unknown
          presetId?: unknown
          presetName?: unknown
          tokenSetOrder?: unknown
          semanticTokenPaths?: unknown
        }
      }
      color?: {
        $description?: unknown
        blue?: {
          $description?: unknown
          "600"?: {
            $value?: unknown
            $type?: unknown
          }
        }
      }
      button?: {
        radius?: {
          $value?: unknown
          $type?: unknown
        }
      }
      "font-size"?: {
        base?: {
          $value?: unknown
          $type?: unknown
        }
      }
      "letter-spacing"?: {
        normal?: {
          $value?: unknown
          $type?: unknown
        }
      }
      "line-height"?: {
        normal?: {
          $value?: unknown
          $type?: unknown
        }
      }
      size?: {
        "10"?: {
          $value?: unknown
          $type?: unknown
        }
      }
      typography?: {
        family?: {
          sans?: {
            $value?: unknown
            $type?: unknown
          }
        }
        body?: {
          md?: {
            fontFamily?: {
              $value?: unknown
              $type?: unknown
            }
          }
        }
      }
    }

    expect(json.$schema).toBe(
      "https://www.designtokens.org/schemas/2025.10/format.json",
    )
    expect(json.$extensions?.["org.neurex"]).toMatchObject({
      generatedBy: "@neurex/tokens",
      presetId: "neurex",
      presetName: "Neurex Default",
      tokenSetOrder: ["primitives", "semantics", "components"],
    })
    expect(json.$extensions?.["org.neurex"]?.semanticTokenPaths).toContain(
      "color.background.base",
    )
    expect(json.$extensions?.["org.neurex"]?.semanticTokenPaths).toContain(
      "typography.body.md.fontFamily",
    )
    expect(json.color?.$description).toBe(
      "Raw color palette. Never use directly in components.",
    )
    expect(json.color?.blue?.$description).toBe(
      "Blue primitive palette for brand, primary action, and information mappings.",
    )
    expect(json.color?.blue?.["600"]).toEqual({
      $value: "oklch(0.455 0.191 259.631)",
      $type: "color",
    })
    expect(json.button?.radius).toEqual({
      $value: "{radius.control}",
      $type: "dimension",
    })
    expect(json["font-size"]?.base).toEqual({
      $value: "1rem",
      $type: "fontSize",
    })
    expect(json["letter-spacing"]?.normal).toEqual({
      $value: "0",
      $type: "letterSpacing",
    })
    expect(json["line-height"]?.normal).toEqual({
      $value: 1.5,
      $type: "number",
    })
    expect(json.size?.["10"]).toEqual({
      $value: "2.5rem",
      $type: "dimension",
    })
    expect(json.typography?.family?.sans).toEqual({
      $value: "{font-family.sans}",
      $type: "fontFamily",
    })
    expect(json.typography?.body?.md?.fontFamily).toEqual({
      $value: "{typography.family.sans}",
      $type: "fontFamily",
    })
  })

  test("generates DTCG-compatible theme json from token source", () => {
    const outputs = createStyleOutputs()
    const json = JSON.parse(outputs.themesJson) as {
      $extensions?: {
        "org.neurex"?: {
          tokenSetOrder?: unknown
          themes?: unknown
        }
      }
      light?: {
        color?: {
          background?: {
            base?: {
              $value?: unknown
            }
          }
        }
      }
      dark?: Record<string, unknown>
    }

    expect(json.$extensions?.["org.neurex"]?.tokenSetOrder).toEqual(["themes"])
    expect(json.$extensions?.["org.neurex"]?.themes).toEqual([
      {
        name: "light",
        brand: "neurex",
        selector: ":root",
        colorScheme: "light",
      },
      {
        name: "dark",
        brand: "neurex",
        selector: ".dark",
        colorScheme: "dark",
      },
    ])
    expect(json.light?.color?.background?.base?.$value).toBe("{color.white}")
    expect(json.light).not.toHaveProperty("selector")
    expect(json.light).not.toHaveProperty("colorScheme")
    expect(json.dark).not.toHaveProperty("selector")
    expect(json.dark).not.toHaveProperty("colorScheme")
  })

  test("parses generated DTCG json back into a token tree", () => {
    const outputs = createStyleOutputs()
    const input = createDtcgTokenInputFromJson(outputs.tokensJson)

    expect(input.metadata).toMatchObject({
      generatedBy: "@neurex/tokens",
      presetId: "neurex",
      presetName: "Neurex Default",
      tokenSetOrder: ["primitives", "semantics", "components"],
    })
    expect("$schema" in input.tokenTree).toBe(false)
    expect("$extensions" in input.tokenTree).toBe(false)
    expect(input.tokenTree.color).toBeDefined()
    expect(input.tokenTree.button).toBeDefined()
    expect(input.semanticTokenTree?.color).toBeDefined()
    expect(input.semanticTokenTree?.button).toBeUndefined()
  })

  test("generates tokens css from DTCG json input", () => {
    const outputs = createStyleOutputs()
    const css = createTokensCssFromDtcgJson(outputs.tokensJson)

    expect(css).toContain("Generated by @neurex/tokens. Do not edit directly.")
    expect(css).toContain("--nx-color-blue-600")
    expect(css).toContain("--nx-radius-control: var(--nx-radius-md)")
    expect(css).toContain("--nx-button-radius: var(--nx-radius-control)")
    expect(css).not.toContain("--nx-$schema")
    expect(css).not.toContain("--nx-$extensions")
  })

  test("generates theme css from DTCG json input", () => {
    const outputs = createStyleOutputs()
    const css = createThemeCssFromDtcgJson(
      outputs.tokensJson,
      outputs.themesJson,
    )

    expect(css).toBe(outputs.themeCss)
    expect(css).toContain(".dark")
    expect(css).toContain("color-scheme: light;")
    expect(css).toContain("color-scheme: dark;")
    expect(css).toContain("@theme inline")
    expect(css).toContain("--color-nx-background-base")
    expect(css).not.toContain("--color-nx-blue-600")
    expect(css).not.toContain("--color-nx-button")
    expect(css).not.toContain("--nx-$schema")
    expect(css).not.toContain("--nx-$extensions")
  })

  test("rejects theme css generation without semantic token path metadata", () => {
    const outputs = createStyleOutputs()
    const tokensJson = JSON.parse(outputs.tokensJson) as {
      $extensions: {
        "org.neurex": {
          semanticTokenPaths?: unknown
        }
      }
    }

    delete tokensJson.$extensions["org.neurex"].semanticTokenPaths

    expect(() => {
      createThemeCssFromDtcgJson(JSON.stringify(tokensJson), outputs.themesJson)
    }).toThrow('DTCG token document extension "org.neurex" is missing')
  })

  test("rejects theme css generation without theme metadata", () => {
    const outputs = createStyleOutputs()
    const themesJson = JSON.parse(outputs.themesJson) as {
      $extensions: {
        "org.neurex": {
          themes?: unknown
        }
      }
    }

    delete themesJson.$extensions["org.neurex"].themes

    expect(() => {
      createThemeCssFromDtcgJson(outputs.tokensJson, JSON.stringify(themesJson))
    }).toThrow('DTCG theme document extension "org.neurex" is missing')
  })

  test("keeps authoring tokens prefix-free", () => {
    const tokenSource = JSON.stringify({
      componentTokens,
      primitiveTokens,
      semanticTokens,
      themes,
    })

    expect(tokenSource).not.toContain("--nx")
    expect(tokenSource).not.toContain("var(--")
    expect(tokenSource).not.toContain("{line-height.6}")
    expect(tokenSource).toContain("{motion.duration.fast}")
  })
})
